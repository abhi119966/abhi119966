									<span class="historyDescriptionHeadingstyle">History of Kutch</span><br><br><br>
								The history of Kutch dates back to prehistoric times. There have been traces found, of its existence, in the Indus Valley sites. Alexander mentioned the town in his
early writings. It was ruled by the Greeks before it nally fell in the hands of the Mauryan Empire. For the next few centuries, Kutch was ruled by a number of
dynasties including Western Satraps, Guptas, Chavdas, Chalukyas and the Jadega brothers. The ancestors of the last ruler Rayadhan II, also found Kathiawar. In

	<br><br><br>
	<span class="historyDescriptionHeadingstyle">History of Mumbai</span><br><br><br>
	The history of the city of Mumbai, or Bombay, began with the signing of the Treaty of Bassein between Sultan Bahadur Shah of Gujarat and the Portuguese, who
were oered the seven islands that made up Bombay by the Sultan. The islands were initially referred to by several dierent names, but collectively took the
written name of Bombain. The Portuguese built several churches and forts in the city during their reign. With its growing strategic importance as a natural
harbour, it attracted repeated British and Dutch interests. In 1661, as part of the royal marriage alliance between Charles II of England and Princess Catherine of
Portugal, the islands were given as dowry to the British by the Portuguese. In 1668, the English leased 3 of the islands to the British East India Company, and a few
years later, they shifted their capital from Surat to Bombay. Bombay was thus the capital of the Bombay Presidency and quickly gained commercial and military
signicance. The Portuguese nally left Bombay in the 1730s, following their defeat to the Marathas under Peshwa Baji Rao. Following the Anglo-Maratha war and
the signing of some treaties, the British drove out the Marathas and were able to establish their majoritarian supremacy over Bombay. Under the Hornby Vellard
project, the islands were unied through signicant land reclamation activities into a single landmass. Bombay was extremely active in the political sphere during
India?s independence struggle and was part of the Quit India Movement & the Royal Navy Mutiny in the 1940s.



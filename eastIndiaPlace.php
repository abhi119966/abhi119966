								Culture is a blend of mind and spirit where people exist in an environment with their growing art, customs and practices. Puri, in Odisha, adapts its culture from
various festivals which are celebrated through the year. Amongst these festivals, Rath Yatra at Jagannath Temple is of great signicance. In addition to these

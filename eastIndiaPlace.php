									<span class="historyDescriptionHeadingstyle">History of Darjeeling</span><br><br><br>
							The name Darjeeling means the "the land of the thunderbolt", derived from the Tibet word "Dorje" meaning "thunder" and "ling" meaning ?piece of land?. Prior to
its acquisition by East India Company, Darjeeling used to be a part of Sikkim and even before that, a part of Nepal. In February 1829, a dispute broke out between
Nepal and Sikkim about their borders and hence, Lord William Bentinck- Governor General of India at the time, set out to resolve the situation. In the process, he
realised that the quaint hill town had strategic advantages for trade and military outpost. Later, it was also realised that the land was ideal for tea plantations. The
district underwent major development and became a major tourist attraction visited by the British ocials. It, therefore, earned itself the title of "Queen of hills".
After independence, the district was merged with West Bengal and since then is one of the most popular hill stations in India.
	<br><br><br>
	<span class="historyDescriptionHeadingstyle">Culture of Darjeeling</span><br><br><br>
	Darjeeling has a unique and diverse culture. The city is a home to mix immigrants from Nepal, Sikkim, Tibet, Bhutan and even Europe, due to work opportunities
in tea plantations in the bygone era. Hinduism and Vajyarana Buddhism are the main religions of the people. One of the major festivals of Darjeeling is Losar,
primarily their new year, which is celebrated in January or February depending on the position of the moon (lunar calendar). Darjeeling Carnival is another major
festival held annually. The fair involves a display of folk dance and arts. Local rock bands participate in the same. Other important festivals are Makar Sankranti,
Chotrul Duchen (Buddhist festival commemorating the four events in the life of Gautam Buddha), Tendong Lho Rumfaat (Prayer of the Tendong mountain) etc.
							

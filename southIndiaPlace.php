									<span class="historyDescriptionHeadingstyle">History of Mahabalipuram</span><br><br><br>
								Mahabalipuram or Mamallapuram was once a trade city that was established during the Pallava dynasty. It became an important port and was a ourishing and
vibrant centre of activity. The Pallavas left behind a treasure-trove of history, architecture and culture in the form of stunning monoliths and rock temples, and
sculptures dedicated to the Gods. The history of this town stretches even beyond the 7th & 8th centuries and the Pallava dynasty; settlements belonging to the
pre-Sangam era and artefacts that predate the birth of Christ have also been found here. There is thus an air of mystery surrounding Mahabalipuram?s history,

	<br><br><br>
	<span class="historyDescriptionHeadingstyle">Cultural Influence On Architecture In Mahabalipuram</span><br><br><br>
	Mahabalipuram is famous for its architectural wonders. These monuments, revering the various Gods and holy scriptures of the Hindu religion, are mainly in the
form of temples, rock-cut caves and other stone inscriptions. These monuments are classied into ve major forms - the Rathas or chariot temples, Mandapas or
cave temples, rock relief structures, structural temples and excavations. The Rathas are found in the southern part of the town, the most famous being the Panch
Rathas, dedicated to the 5 Pandava brothers from the Mahabharata. The Mandapas were historically places for social gatherings to take place. They have
excavated caves depicting various intricate carvings from Hindu mythology. Among the temples, the Shore temple located along the sea is the most popular,
mainly dedicated to the Lords Shiva and Vishnu. Rock reliefs, as the name suggests, are carved on independent rocks or big stones. The rock relief structure,
Descent of the Ganges or Arjuna?s penance, is believed to be one of the largest in the world and is carved on two large boulders 27m long and 9m high.


							
